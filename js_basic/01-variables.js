// let: las variables let solo son accesibles dentro de un bloque
let helloWorld2 = "Hello, JavaScript con let"
console.log(helloWorld2)

helloWorld2 = "Hello, JavaScript con let, reasignada"
console.log(helloWorld2)


helloWorld2 = 5
console.log(helloWorld2)


